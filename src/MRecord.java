public class MRecord {
    String itemName;
    boolean isExpense;
    int quantity;
    int sumOfOne;
    int month;

    public MRecord (String itemName, boolean isExpense, int quantity, int sumOfOne, int month) {
        this.itemName = itemName;
        this.isExpense = isExpense;
        this.quantity = quantity;
        this.sumOfOne = sumOfOne;
        this.month = month;
    }

}



